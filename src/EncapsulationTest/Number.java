package EncapsulationTest;

public class Number {
	
	private int i;
	private String s;
	private float f;
	
	public int getI() {
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}
	public String getS() {
		return s;
	}
	public void setS(String s) {
		this.s = s;
	}
	public float getF() {
		return f;
	}
	public void setF(float f) {
		this.f = f;
	}
	

}
