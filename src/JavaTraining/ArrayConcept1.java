package JavaTraining;

import java.util.Scanner;

public class ArrayConcept1 {

	public static void main(String[] args) {
	
		for(int i=0;i<4;i++)
		{
			for(int d=0;d<10;d++)
			{
				System.out.print(i);
				System.out.print(d+ " ");
				
			}
			System.out.println();
		}
		
		System.out.println("------------------------------");
		for(int i=4;i>=0;i--)
		{
			System.out.println("n = "+i);
		}
	}

}
