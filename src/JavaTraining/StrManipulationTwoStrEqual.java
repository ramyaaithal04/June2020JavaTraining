package JavaTraining;

//  program to check two different strings equality

public class StrManipulationTwoStrEqual {

	public static void main(String[] args)
	{
		String str1, str2;
		
		str1="Ramya";
		str2="ramya";
		
		if (str1.equals(str2))
		{
			System.out.println("Two strings are equal.");			
		}
		else
		{
			System.out.println("Two Strings are not equal.");
		}

	}

}
